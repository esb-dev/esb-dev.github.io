/* Die Datenbank Literatur aus der Veranstaltung Datenbanksysteme
*/

-- table Autor 
create table Autor (
  AId      integer primary key, 
  Name	   varchar (20) not null, 
  Vorname  varchar (20)
);

-- table Buch 
create table Buch (
  BId        integer primary key, 
  Titel	     varchar (80) not null, 
  Verlag     varchar (40), 
  Ort        varchar (40), 
  Jahr	     varchar (4), 
  ISBN	     varchar (13), 
  Anmerkung  varchar (60) 
);

-- table BA Autorenschaft
create table BA (
  BId    integer references Buch, 
  AId    integer references Autor,
  primary key (BId, AId)
);

-- table Sachverz Sachverzeichnis
create table Sachverz (
  SId         integer primary key, 
  Schlagwort  varchar (30) not null
);

-- table BS Schlagwortzuordnung
create table BS (
  BId    integer references Buch, 
  SId    integer references Sachverz,
  primary key (BId, SId)
);

